const mapTransform = (map: CharacterMap) => (text: string) => text.split('').map(char => map[char] || char).join('');

const createReverseMap = (map: CharacterMap): CharacterMap => {
  return Object.fromEntries(Object.entries(map).map(([key, value]) => [value, key]));
};

const allMaps = [boldMap, italicMap, boldItalicMap, cursiveMap, monospaceMap];
const combinedReverseMap: CharacterMap = allMaps.reduce((acc, currentMap) => {
  return { ...acc, ...createReverseMap(currentMap) };
}, {});

const clearFormattingTransform = (text: string): string => {
  return text.split('').map(char => combinedReverseMap[char] || char).join('');
};

